import type { ReactNode, SVGProps } from "react";

/**
 * Small inline-SVG icons for the landing — the glyphs the prototype drew with
 * lucide, hand-authored here so we carry no icon dependency and ship no client
 * JS. They inherit color via `currentColor`, scale with the `size` prop, and are
 * decorative by default (`aria-hidden`), so they never pollute the accessible
 * name of a button or link that already has a text label.
 */
export type IconProps = Omit<SVGProps<SVGSVGElement>, "children"> & {
  /** Width and height in px. */
  size?: number;
};

function Icon({ size = 24, children, ...props }: IconProps & { children: ReactNode }) {
  return (
    <svg
      width={size}
      height={size}
      viewBox="0 0 24 24"
      fill="none"
      stroke="currentColor"
      strokeWidth={2}
      strokeLinecap="round"
      strokeLinejoin="round"
      aria-hidden="true"
      focusable="false"
      {...props}
    >
      {children}
    </svg>
  );
}

